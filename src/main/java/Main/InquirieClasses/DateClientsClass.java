package Main.InquirieClasses;

public class DateClientsClass
{
    private String date;
    private String first;
    private String middle;
    private String last;
    private String quantity;
    private String name;

    public String getDate() { return date; } public void setDate(String date) { this.date = date; }
    public String getFirst() { return first; } public void setFirst(String first) { this.first = first; }
    public String getMiddle() { return middle; } public void setMiddle(String middle) { this.middle = middle; }
    public String getLast() { return last; } public void setLast(String last) { this.last = last; }
    public String getQuantity() { return quantity; } public void setQuantity(String quantity) { this.quantity = quantity; }
    public String getName() { return name; } public void setName(String name) { this.name = name; }
}
